        <h2 class="d-flex align-items-center justify-content-center pb-3 pt-5">
            what happens on the day?
        </h2>

        <p>
            A typical university competition sees the team travel down to the
            competition by coach. We leave early on the morning of the
            competition, with pick-up points in Leamington Spa and on campus.
            The exception to this is Nationals in Blackpool, where we leave
            Friday afternoon and stay in a hotel for two nights.
        </p>

        <p>
            Once we arrive at the venue, we change into our dancewear to start
            the competition. Ballroom is normally in the morning and Latin in
            the afternoon, with Team Match in the evening. After the competition
            is finished, we travel home on the coach and get dropped off on
            campus or in Leamington Spa. On the journey home, the Team Captains
            will lead a debrief and congratulate couples on their performance. A
            Couple of the Competition is selected for the couple who have had a
            stand-out day.
        </p>

